import wave
import audioop
import os, sys

def downsampleWav(src, dst, inrate=44100, outrate=16000, inchannels=2, outchannels=1):
    if not os.path.exists(src):
        print('Source not found!')
        return False

    if not os.path.exists(os.path.dirname(dst)):
        os.makedirs(os.path.dirname(dst))

    try:
        s_read = wave.open(src, 'rb')
        s_write = wave.open(dst, 'wb')
    except:
        print('Failed to open files!')
        return False

    n_frames = s_read.getnframes()
    data = s_read.readframes(n_frames)

    try:
        converted = audioop.ratecv(data, 2, inchannels, inrate, outrate, None)
        if outchannels == 1:
            converted = audioop.tomono(converted[0], 2, 1, 0)
    except:
        print('Failed to downsample wav')
        return False

    try:
        s_write.setparams((outchannels, 2, outrate, 0, 'NONE', 'Uncompressed'))
        s_write.writeframes(converted)
    except:
        print('Failed to write wav')
        return False

    try:
        s_read.close()
        s_write.close()
    except:
        print('Failed to close wav files')
        return False

    return True
    
src_root = "/home/bijan/Project/ProjectZ/kaldi/egs/digits/digits_audio/"

for root, subdirs, files in os.walk(src_root):	
	for file in files:
		filePath = os.path.join(root, file)
		dstFilePath = filePath.replace("digits_audio", "digits_audio_16")
		downsampleWav(filePath, dstFilePath)


