#!/usr/bin/lua

function get_Digit(word)

	if word == "zero" then
		return "11"
	elseif word == "one" then
		return "2"
	elseif word == "two" then
		return "3"
	elseif word == "three" then
		return "4"
	elseif word == "four" then
		return "5"
	elseif word == "five" then
		return "6"
	elseif word == "six" then
		return "7"
	elseif word == "seven" then
		return "8"
	elseif word == "eight" then
		return "9"
	elseif word == "nine" then
		return "10"
	end

end