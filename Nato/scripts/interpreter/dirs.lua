#!/usr/bin/lua

function get_Dirs(word)

	if word == "back" then
		return "14"
	elseif word == "left" then
		return "105"
	elseif word == "down" then
		return "108"
	elseif word == "raise" then
		return "103"
	elseif word == "right" then
		return "106"
	elseif word == "slap" then
		return "28"
	elseif word == "tab" then
		return "15"
	elseif word == "delete" then
		return "111"
	elseif word == "departure" then
		return "1"
	end

end